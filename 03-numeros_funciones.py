import math #se importa libreria de python para tener mas metodos para uso matematico

print(5 + 15)
print(5 - 15)
print(5 * 15)
print(5 / 15)
print(5 // 15) #Sin mostrar los decimales
print(5 ** 3) #POTENCIA
print(10 % 3) #MODULO
print(round(1.7)) #devuele el numero mas cercano al numero puesto en la funcion
print(abs(-77)) #entrega el valor absoluto que esta dentro de la funcion

#estas seran funcion es que hacen uso de la libreria math
print(f"\nFunciones con import math")
print(math.ceil(1.1)) #muestra el numero mas cercano hacia arriba
print(math.floor(1.999)) #muestra el numero mas cercano hacia abajo
print(math.isnan(23)) 
print(math.pow(10, 3)) #esto es para la potencia en este caso es 10 elevado a 3
print(math.sqrt(9)) #para sacar raiz cuadrada